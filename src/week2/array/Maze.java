package week2.array;

public class Maze  {

    int x;
    int y;

    public Maze(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "Maze{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
